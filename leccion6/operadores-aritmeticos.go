package main

import "fmt"

func main() {
	var numero1 int = 10
	var numero2 int = 2

	//Suma
	fmt.Println("Sumar: ")
	fmt.Println(numero1 + numero2)

	//Resta
	fmt.Println("Restar:")
	fmt.Println(numero1 - numero2)

	//Multiplacion
	fmt.Println("Multiplicación:")
	fmt.Println(numero1 * numero2)

	//División
	fmt.Println("División:")
	fmt.Println(numero1 / numero2)

	//Resto
	fmt.Println("Resto:")
	fmt.Println(numero1 % numero2)
}
